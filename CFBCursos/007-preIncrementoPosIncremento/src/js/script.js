let num1 = 10
let num2 = -10
let x1 =-num1
let x2 =-num2

//incremento ++
alert(`
    incremento\n
    valor original: ${num1}\n
    pós incremento: ${num1++}\n
    valor original: ${num1}\n
    pré incremento: ${++num1}\n
    valor original: ${num1}\n
    `)

//decremento --
alert(`
    decremento\n
    valor original: ${num1}\n
    pós decremento: ${num1--}\n
    valor original: ${num1}\n
    pré decremento: ${--num1}\n
    valor original: ${num1}\n
    `)

//inversão
alert( `
    inversão\n
    valor original num1: ${num1}\n
    let x =- num1 = ${x1}\n
    valor original num2: ${num2}\n
    let x =- num2 = ${x2}\n
    `)

//concatenação e soma
alert( `
    number e number\n
    valor original num1: ${num1}\n
    num1 + 10 = ${num1+10}\n
    valor original num2: ${num1}\n
    num1 + "10" = ${num1+"10"}\n
    `)